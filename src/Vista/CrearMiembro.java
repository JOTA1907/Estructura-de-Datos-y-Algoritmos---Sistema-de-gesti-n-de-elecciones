    private void CrearMiembrobtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearMiembrobtnActionPerformed

        boolean correcto = false;
        
        String dni = this.DNItxtField.getText();
        String nombre = this.NombretxtField.getText();
        String apellido = this.ApellidotxtField.getText();
        
        String tipo = "";
        if(this.radiobotonpresidente.isSelected()){
            tipo = "Presidente";
        }
        else if(this.radiobotonsecretario.isSelected()){
            tipo = "Secretario";
        }
        else if(this.radiobotonvocal.isSelected()){
            tipo = "Vocal";
        }       
        
        String clave = this.ClavetxtField.getText();
        
        for (Eleccion eleccion : TRABAJOEDAGRUPAL.Elecciones) {
            if (Integer.parseInt(this.eleccionid) == eleccion.getId()) {
                if (!dni.isEmpty() && !nombre.isEmpty() && !apellido.isEmpty() && !tipo.isEmpty() && !clave.isEmpty()){
                    TRABAJOEDAGRUPAL.buscarEleccionPorId(Integer.parseInt(eleccionid)).BuscarMesa(numeroMesa).crearMiembro(dni, nombre, apellido, tipo, clave);
                    correcto = true;
                }
                else{
                    JOptionPane.showMessageDialog(null, "Complete todos los campos");
                    correcto = false;
                }
            }    
        }

        if (correcto == true) {
//            JOptionPane.showMessageDialog(null, "Miembro creado correctamente");
            MenuMesas menuMesas = new MenuMesas(eleccionid);
            menuMesas.setVisible(true);
            this.setVisible(false);
        }
        else{
           JOptionPane.showMessageDialog(null, "No se pudo crear el miembro");
        }

    }//GEN-LAST:event_CrearMiembrobtnActionPerformed

