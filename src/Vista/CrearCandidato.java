    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:
        if (eleccionSeleccionada == null) {
        JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna elecci√≥n.");
        return;
        }
        
        
        String dni = txtdni.getText();
        String nombre = txtnombre.getText();
        String apellidos = txtapellidos.getText();
        String partido = txtpartido.getText();
        
        
        Candidato candidato = new Candidato(dni,nombre,apellidos,partido);
        eleccionSeleccionada.getListaCandidatos().addLast(candidato);

        JOptionPane.showMessageDialog(null, "Candidato agregado");

        this.txtdni.setText("");
        this.txtnombre.setText("");
        this.txtapellidos.setText("");
        this.txtpartido.setText("");
        CargarTablaCandidatos();

    }//GEN-LAST:event_agregarActionPerformed

