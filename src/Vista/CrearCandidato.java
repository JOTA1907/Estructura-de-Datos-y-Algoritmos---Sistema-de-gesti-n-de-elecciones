    public void CargarTablaCandidatos(){
        
        int filas = this.TablaCandidatos.getRowCount();
        
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);      
        }
        if (eleccionSeleccionada == null || eleccionSeleccionada.getListaCandidatos().isEmpty()) {
           JOptionPane.showMessageDialog(null, "No hay candidatos para mostrar"); 
        }
      
        String[] datos = new String[4];
        for (Candidato candidato : eleccionSeleccionada.getListaCandidatos()) {
            datos[0] = String.valueOf(candidato.getDni());
            datos[1] = String.valueOf(candidato.getNombre());
            datos[2] = String.valueOf(candidato.getApellidos());
            datos[3] = String.valueOf(candidato.getPartido());

            modelo.addRow(datos);
        }
       
    }
