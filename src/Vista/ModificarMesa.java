    private void ModMesabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModMesabtnActionPerformed

        boolean correcto = false;
        
        String ubicacion = NuevaUbicaciontxtField.getText();
        for (Eleccion eleccion : TRABAJOEDAGRUPAL.Elecciones) {    
            if (Integer.parseInt(this.eleccionid) == eleccion.getId()) {
           
                if (!ubicacion.isEmpty()) {
                    eleccion.modificarMesa(numeroMesa, ubicacion);
                    correcto = true;

                } else {
                    JOptionPane.showMessageDialog(null, "Ingrese una ubicacion");
                    correcto = false;
                }
            }     
        }                 

        if (correcto == true) {
//            JOptionPane.showMessageDialog(null, "Mesa modificada correctamente");
            MenuMesas menuMesas = new MenuMesas(eleccionid);
            menuMesas.setVisible(true);
            this.setVisible(false);          
        }
        else{
           JOptionPane.showMessageDialog(null, "No se pudo modificar la mesa");
        }

    }//GEN-LAST:event_ModMesabtnActionPerformed

