    public Votos(String cantidato, int idElec, int idMesa) {
        this.candidato = cantidato;
        this.idElec = idElec;
        this.idMesa = idMesa;
        initComponents(); //INCIALIZA TODOS LOS COMPONENTES
        for (Candidato candidato : TRABAJOEDAGRUPAL.buscarEleccionPorId(idElec).getListaCandidatos()) {
            if (candidato.getDni().equals(cantidato)) {
                resultado = "Candidato: "+candidato.getNombre() + "\n"+
                            candidato.getApellidos()+ "\n"+
                            "Partido: " + candidato.getPartido();
            }
        }
        this.votofield.setText(resultado);
        this.MostrarMesaLabel.setText(Integer.toString(idMesa));
        this.MostrarEleccionLabel.setText(Integer.toString(idElec));
        
 
    }
