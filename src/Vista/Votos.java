    private void ConfirmarVotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarVotoActionPerformed
        // TODO add your handling code here:
        if(!votofield.getText().isEmpty()){
            if (this.nulo) {
            Voto v1= new Voto("valido",candidato);
             
            TRABAJOEDAGRUPAL.buscarEleccionPorId(idElec).BuscarMesa(idMesa).getVotos().add(v1);
            }else{
                Voto v1= new Voto("nulo");

                TRABAJOEDAGRUPAL.buscarEleccionPorId(idElec).BuscarMesa(idMesa).getVotos().add(v1);
            }
            MenuMesas votar = new MenuMesas(Integer.toString(idElec));
            votar.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null,"Voto no v√°lido");
        }
        
        
    
    }//GEN-LAST:event_ConfirmarVotoActionPerformed

