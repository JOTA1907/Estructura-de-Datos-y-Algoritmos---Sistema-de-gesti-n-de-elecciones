    private void ModMiembrobtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModMiembrobtnActionPerformed

        boolean correcto = false;
        
        String dni = this.NuevoDNItxtField.getText();
        String nombre = this.NuevoNombretxtField1.getText();
        String apellido = this.NuevoApellidotxtField.getText();
        
        String tipo = "";
        if(this.radiobotonpresidente.isSelected()){
            tipo = "Presidente";
        }
        else if(this.radiobotonsecretario.isSelected()){
            tipo = "Secretario";
        }
        else if(this.radiobotonvocal.isSelected()){
            tipo = "Vocal";
        }       
        
        String clave = this.NuevoClavetxtField.getText();
        
        for (Eleccion eleccion : TRABAJOEDAGRUPAL.Elecciones) {
            if (Integer.parseInt(this.eleccionid) == eleccion.getId()) {
                if (!dni.isEmpty() && !nombre.isEmpty() && !apellido.isEmpty() && !tipo.isEmpty() && !clave.isEmpty()){
                    TRABAJOEDAGRUPAL.buscarEleccionPorId(Integer.parseInt(eleccionid)).BuscarMesa(numeroMesa).modificarMiembro(tipoMiembro , dni, nombre, apellido, tipo, clave);
                    correcto = true;
                }
                else{
                    JOptionPane.showMessageDialog(null, "Complete todos los campos");
                    correcto = false;
                }
            }    
        }

        if (correcto == true) {
            MenuMesas menuMesas = new MenuMesas(eleccionid);
            menuMesas.setVisible(true);
            this.setVisible(false);
        }
        else{
           JOptionPane.showMessageDialog(null, "No se pudo crear el miembro");
        }

    }//GEN-LAST:event_ModMiembrobtnActionPerformed

